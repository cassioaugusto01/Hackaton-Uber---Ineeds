package Pedido;

public class Processo {

	private String receber_ped;
	private String origem;
	private String destino;

	public String getReceber_ped() {
		return receber_ped;
	}

	public void setReceber_ped(String receber_ped) {
		this.receber_ped = receber_ped;
	}

	public String getOrigem() {
		return origem;
	}

	public void setOrigem(String origem) {
		this.origem = origem;
	}

	public String getDestino() {
		return destino;
	}

	public void setDestino(String destino) {
		this.destino = destino;
	}

}